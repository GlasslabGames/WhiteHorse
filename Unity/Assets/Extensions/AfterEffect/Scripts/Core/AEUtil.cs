	//--------------------------------------

	//--------------------------------------
	//  PUBLIC METHODS
	//--------------------------------------

	public static void ApplayColor(Material m, Color c) {
	
		if(m.HasProperty("_Color")) {
			m.color = c;
		}  else {
			if(m.HasProperty ("_TintColor")) {
				m.SetColor ("_TintColor", c);
			}
		}

	}
	
